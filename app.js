const languageButtons = [...document.querySelectorAll("[data-lang]")];
const menuButton = document.getElementById("menuButton");
const navigation = document.getElementById("siteNav");
const metaDescription = document.querySelector('meta[name="description"]');
const ogTitle = document.querySelector('meta[property="og:title"]');
const ogDescription = document.querySelector('meta[property="og:description"]');
const ogImageAlt = document.querySelector('meta[property="og:image:alt"]');
const supportedLanguages = ["en", "tr"];

const readStoredLanguage = () => {
  try {
    return window.localStorage.getItem("aeon-investor-language");
  } catch {
    return null;
  }
};

const writeStoredLanguage = (language) => {
  try {
    window.localStorage.setItem("aeon-investor-language", language);
  } catch {
    return;
  }
};

const queryLanguage = new URLSearchParams(window.location.search).get("lang");
const storedLanguage = readStoredLanguage();
let currentLanguage = supportedLanguages.includes(queryLanguage)
  ? queryLanguage
  : supportedLanguages.includes(storedLanguage)
    ? storedLanguage
    : "en";

const setMenuState = (isOpen) => {
  document.body.classList.toggle("nav-open", isOpen);
  menuButton.setAttribute("aria-expanded", String(isOpen));
  menuButton.setAttribute("aria-label", isOpen ? pageMetadata[currentLanguage].menuClose : pageMetadata[currentLanguage].menuOpen);
};

const setLocalizedAccessibility = (language) => {
  const labels = pageMetadata[language];
  document.querySelector(".brand")?.setAttribute("aria-label", labels.home);
  navigation?.setAttribute("aria-label", labels.navigation);
  document.querySelector(".language-switch")?.setAttribute("aria-label", labels.language);
  document.querySelector(".hero-visual")?.setAttribute("aria-label", labels.heroVisual);
  document.querySelector(".proof-row")?.setAttribute("aria-label", labels.proof);
  document.querySelectorAll("img").forEach((image, index) => {
    if (imageAlternatives[language][index]) image.alt = imageAlternatives[language][index];
  });
};

const applyLanguage = (language, updateAddress = false) => {
  if (!supportedLanguages.includes(language)) return;
  currentLanguage = language;
  document.body.classList.add("language-changing");
  document.documentElement.lang = language;
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (translations[language][key] !== undefined) element.textContent = translations[language][key];
  });
  languageButtons.forEach((button) => {
    const isActive = button.dataset.lang === language;
    button.classList.toggle("active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });
  const metadata = pageMetadata[language];
  document.title = metadata.title;
  metaDescription.content = metadata.description;
  ogTitle.content = metadata.ogTitle;
  ogDescription.content = metadata.ogDescription;
  ogImageAlt.content = metadata.previewAlt;
  setLocalizedAccessibility(language);
  setMenuState(false);
  writeStoredLanguage(language);
  if (updateAddress) {
    const url = new URL(window.location.href);
    if (language === "tr") url.searchParams.set("lang", "tr");
    else url.searchParams.delete("lang");
    window.history.replaceState({}, "", url);
  }
  window.setTimeout(() => document.body.classList.remove("language-changing"), 150);
};

languageButtons.forEach((button) => {
  button.addEventListener("click", () => applyLanguage(button.dataset.lang, true));
});

menuButton.addEventListener("click", () => {
  setMenuState(!document.body.classList.contains("nav-open"));
});

navigation.addEventListener("click", (event) => {
  if (event.target.closest("a")) setMenuState(false);
});

document.addEventListener("click", (event) => {
  if (!document.body.classList.contains("nav-open")) return;
  if (!navigation.contains(event.target) && !menuButton.contains(event.target)) setMenuState(false);
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape") setMenuState(false);
});

const revealElements = [...document.querySelectorAll(".reveal")];
if ("IntersectionObserver" in window && !window.matchMedia("(prefers-reduced-motion: reduce)").matches) {
  revealElements.forEach((element, index) => {
    element.classList.add("is-pending");
    element.style.transitionDelay = `${Math.min(index % 3, 2) * 45}ms`;
  });
  const revealObserver = new IntersectionObserver((entries, observer) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) return;
      entry.target.classList.add("is-visible");
      entry.target.classList.remove("is-pending");
      observer.unobserve(entry.target);
    });
  }, { threshold: 0.08, rootMargin: "0px 0px -6% 0px" });
  revealElements.forEach((element) => revealObserver.observe(element));
}

const navLinks = [...navigation.querySelectorAll('a[href^="#"]')];
const navSections = navLinks
  .map((link) => document.querySelector(link.getAttribute("href")))
  .filter(Boolean);

if ("IntersectionObserver" in window) {
  const sectionObserver = new IntersectionObserver((entries) => {
    const visible = entries
      .filter((entry) => entry.isIntersecting)
      .sort((a, b) => b.intersectionRatio - a.intersectionRatio)[0];
    if (!visible) return;
    navLinks.forEach((link) => {
      const active = link.getAttribute("href") === `#${visible.target.id}`;
      link.classList.toggle("active", active);
      if (active) link.setAttribute("aria-current", "location");
      else link.removeAttribute("aria-current");
    });
  }, { threshold: [0.12, 0.3, 0.55], rootMargin: "-16% 0px -62% 0px" });
  navSections.forEach((section) => sectionObserver.observe(section));
}

const scrollProgress = document.getElementById("scrollProgress");
const updateScrollProgress = () => {
  const scrollable = document.documentElement.scrollHeight - window.innerHeight;
  const progress = scrollable > 0 ? Math.min(window.scrollY / scrollable, 1) : 0;
  scrollProgress.style.width = `${progress * 100}%`;
};

window.addEventListener("scroll", updateScrollProgress, { passive: true });
window.addEventListener("resize", () => {
  updateScrollProgress();
  if (window.innerWidth > 800) setMenuState(false);
});

applyLanguage(currentLanguage);
updateScrollProgress();
